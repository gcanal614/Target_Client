    public static void glColor(Color color) {
        GL11.glColor4f(color.getRed() / 255.0f, color.getGreen() / 255.0f, color.getBlue() / 255.0f, color.getAlpha() / 255.0f);
    }

    public static void resetCaps() {
        LiquidRender.glCapMap.forEach(LiquidRender::setGlState);
    }
    
    public static void enableGlCap(int cap) {
        setGlCap(cap, true);
    }
    
    public static void disableGlCap(int... caps) {
        for (int cap : caps) {
            setGlCap(cap, false);
        }
    }
    
    public static void setGlCap(int cap, boolean state) {
        LiquidRender.glCapMap.put(cap, GL11.glGetBoolean(cap));
        setGlState(cap, state);
    }
    
    public static void setGlState(int cap, boolean state) {
        if (state) {
            GL11.glEnable(cap);
        }
        else {
            GL11.glDisable(cap);
        }
    }
